#include "libft.h"

size_t	ft_strlcat(char *dest, const char *src, size_t size)
{
	unsigned int dest_len;
	unsigned int length;
	unsigned int i;

	dest_len = ft_strlen(dest);
	length = dest_len;
	if (size <= length)
		return (ft_strlen(src) + size);
	else
	{
		i = 0;
		while (src[i] && i < size - length - 1)
		{
			dest[dest_len + i] = src[i];
			i++;
		}
		dest[dest_len + i] = '\0';
	}
	return (ft_strlen(src) + length);
}
