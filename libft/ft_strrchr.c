#include "libft.h"

char		*ft_strrchr(const char *s, int c)
{
	int		len;

	len = ft_strlen(s);
	while (s[len] != c && len >= 0)
		len--;
	if (s[len] == (char)c)
		return ((char*)s + len);
	return (NULL);
}
