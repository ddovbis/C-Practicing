void		ft_reverse(char *str)
{
	int		i;
	int		j;
	char	temp;

	i = 0;
	while (str[i] != '\0')
		i++;
	i--;
	j = 0;
	while (j < i)
	{
		temp = str[j];
		str[j] = str[i];
		str[i] = temp;
		j++;
		i--;
	}
}
