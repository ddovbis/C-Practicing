#include "swag.h"

int		nr_retur_line(char *s)
{
	int i;
	int n;

	i = 0;
	n = 1;
	while (s[i] != '\0')
	{
		if (s[i] == '\n')
			n++;
		i++;
	}
	return (n);
}
